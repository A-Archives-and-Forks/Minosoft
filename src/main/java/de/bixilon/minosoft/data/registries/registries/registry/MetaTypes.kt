package de.bixilon.minosoft.data.registries.registries.registry

enum class MetaTypes(val bits: Int) {
    NONE(0),
    BLOCK(4),
    ITEM(16),
    ;

    private val metaMask = (1 shl bits) - 1

    fun modify(id: Int, meta: Int): Int {
        return (id shl bits) or (meta and metaMask)
    }
}
